	/*=============================================
	MOSTRAR PRODUCTOS PARA CATEGORIAS
	=============================================*/

	static public function mdlMostrarProductosUnico($tabla, $item, $valor){

		if($item != null){

			$stmt = Conexion::conectar()->prepare("SELECT * FROM $tabla WHERE $item = :$item ORDER BY id DESC");

			$stmt -> bindParam(":".$item, $valor, PDO::PARAM_STR);

			$stmt -> execute();

