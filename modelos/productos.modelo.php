	/*=============================================
	MOSTRAR PRODUCTOS EXCLUYENDO DESACTIVADOS Y STOCK 0
	=============================================*/

	static public function mdlMostrarProductosActivos($tabla, $item, $valor){

		if($item != null){

			$stmt = Conexion::conectar()->prepare("SELECT * FROM $tabla WHERE $item = :$item and estado = 1 and stock > 0 ORDER BY id DESC");

			$stmt -> bindParam(":".$item, $valor, PDO::PARAM_STR);

			$stmt -> execute();

			return $stmt -> fetch();

		}else{

