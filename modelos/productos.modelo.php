<?php

require_once "conexion.php";

class ModeloProductos{


	/*=============================================
	MOSTRAR SUMA TOTAL DE COSTO DE INVENTARIO
	=============================================*/

	static public function mdlSumaTcostoProductos($tabla){

		

		$stmt = Conexion::conectar()->prepare("SELECT SUM(precio_compra * stock_inicial) AS total_costo FROM $tabla");

		$stmt -> execute();

		return $stmt -> fetchAll();
	
	$stmt -> close();

	$stmt = null;

}

	/*=============================================
	MOSTRAR PRODUCTOS
	=============================================*/

	static public function mdlMostrarProductos($tabla, $item, $valor){

		if($item != null){

			$stmt = Conexion::conectar()->prepare("SELECT * FROM $tabla WHERE $item = :$item ORDER BY id DESC");

			$stmt -> bindParam(":".$item, $valor, PDO::PARAM_STR);

			$stmt -> execute();

			return $stmt -> fetch();

		}else{

			$stmt = Conexion::conectar()->prepare("SELECT * FROM $tabla ORDER BY id DESC");

			$stmt -> execute();

			return $stmt -> fetchAll();

		}

		$stmt -> close();

		$stmt = null;

	}


	/*=============================================
	MOSTRAR PRODUCTOS
	=============================================*/

	static public function mdlMostrarProductosActivados($tabla, $item, $valor){

